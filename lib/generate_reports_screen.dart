import 'package:pdf/pdf.dart';
import 'package:intl/intl.dart';
import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:pdf/widgets.dart' as pw;
import 'package:printing/printing.dart';
import 'package:pie_chart/pie_chart.dart';
import 'package:cloud_firestore/cloud_firestore.dart';
//import 'dart:typed_data';

class GenerateReportsScreen extends StatefulWidget {
  const GenerateReportsScreen({Key? key}) : super(key: key);

  @override
  State<GenerateReportsScreen> createState() => _GenerateReportsScreenState();
}

class _GenerateReportsScreenState extends State<GenerateReportsScreen> {
  int totalCustomers = 0;
  int totalShops = 0;
  int totalRepairRequests = 0;
  int todaysRepairRequests = 0;
  int completedRepairs = 0;
  int pendingRepairs = 0;
  bool isLoading = true;

  @override
  void initState() {
    super.initState();
    _fetchCounts();
  }

  Future<void> _fetchCounts() async {
    try {
      final customersSnapshot = await FirebaseFirestore.instance
          .collection('users')
          .where('role', isEqualTo: 'customer')
          .get();

      final shopsSnapshot = await FirebaseFirestore.instance
          .collection('users')
          .where('role', isEqualTo: 'shop')
          .get();

      final repairRequestsSnapshot = await FirebaseFirestore.instance
          .collection('repair_requests')
          .get();

      final today = DateTime.now();
      final startOfDay = DateTime(today.year, today.month, today.day);
      final startOfDayTimestamp = Timestamp.fromDate(startOfDay);

      final todaysRequestsSnapshot = await FirebaseFirestore.instance
          .collection('repair_requests')
          .where('createdAt', isGreaterThanOrEqualTo: startOfDayTimestamp)
          .get();

      final completedRequestsSnapshot = await FirebaseFirestore.instance
          .collection('repair_requests')
          .where('status', isEqualTo: 'Completed')
          .get();

      final pendingRequestsSnapshot = await FirebaseFirestore.instance
          .collection('repair_requests')
          .where('status', isEqualTo: 'Pending')
          .get();

      setState(() {
        totalCustomers = customersSnapshot.docs.length;
        totalShops = shopsSnapshot.docs.length;
        totalRepairRequests = repairRequestsSnapshot.docs.length;
        todaysRepairRequests = todaysRequestsSnapshot.docs.length;
        completedRepairs = completedRequestsSnapshot.docs.length;
        pendingRepairs = pendingRequestsSnapshot.docs.length;
        isLoading = false;
      });
    } catch (e) {
      print('Error fetching counts: $e');
      setState(() {
        isLoading = false;
      });
    }
  }

  Future<void> _exportPdfReport() async {
    final pdf = pw.Document();
    final ByteData logo = await rootBundle.load('assets/RepairLogo1.png');
    final Uint8List logoBytes = logo.buffer.asUint8List();

    pdf.addPage(
      pw.Page(
        build: (pw.Context context) => pw.Column(
          crossAxisAlignment: pw.CrossAxisAlignment.center,
          children: [
            pw.Center(
              child: pw.Image(pw.MemoryImage(logoBytes), width: 100, height: 100),
            ),
            pw.SizedBox(height: 10),
            pw.Text(
              'Electronics Repair Services Report',
              style: pw.TextStyle(fontSize: 22, fontWeight: pw.FontWeight.bold),
            ),
            pw.Divider(thickness: 2, color: PdfColors.green),
            pw.SizedBox(height: 20),
            pw.Table.fromTextArray(
              headers: ['Metric', 'Value'],
              data: [
                ['Total Customers', totalCustomers.toString()],
                ['Total Shops', totalShops.toString()],
                ['Total Repair Requests', totalRepairRequests.toString()],
                ["Today's New Requests", todaysRepairRequests.toString()],
                ['Completed Repairs', completedRepairs.toString()],
                ['Pending Repairs', pendingRepairs.toString()],
              ],
              headerStyle: pw.TextStyle(
                fontWeight: pw.FontWeight.bold,
                color: PdfColors.white,
              ),
              headerDecoration: pw.BoxDecoration(
                color: PdfColors.green,
              ),
              cellHeight: 30,
              cellAlignments: {
                0: pw.Alignment.centerLeft,
                1: pw.Alignment.centerRight,
              },
            ),
            pw.SizedBox(height: 30),
            pw.Text(
              'Generated by Electronics Repair App',
              style: pw.TextStyle(fontSize: 12, color: PdfColors.grey600),
            ),
            pw.Text(
              'Generated on: ${DateFormat('yyyy-MM-dd – kk:mm').format(DateTime.now())}',
              style: pw.TextStyle(fontSize: 10, color: PdfColors.grey600),
            ),
          ],
        ),
      ),
    );

    await Printing.layoutPdf(onLayout: (format) => pdf.save());
  }

  @override
  Widget build(BuildContext context) {
    if (isLoading) {
      return const Scaffold(
        backgroundColor: Color(0xFFF0F0F0),
        body: Center(child: CircularProgressIndicator(color: Colors.green)),
      );
    }

    Map<String, double> dataMap = {
      "Completed": completedRepairs.toDouble(),
      "Pending": pendingRepairs.toDouble(),
    };

    return Scaffold(
      backgroundColor: const Color(0xFFF0F0F0),
      appBar: PreferredSize(
        preferredSize: const Size.fromHeight(80),
        child: Container(
          decoration: const BoxDecoration(
            color: Color(0xFF39ef64),
            borderRadius: BorderRadius.vertical(bottom: Radius.circular(30)),
          ),
          padding: const EdgeInsets.only(top: 40, left: 20, right: 20, bottom: 10),
          child: Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: [
              Row(
                children: [
                  GestureDetector(
                    onTap: () => Navigator.pop(context),
                    child: const Icon(Icons.arrow_back, color: Colors.white),
                  ),
                  const SizedBox(width: 10),
                  const Icon(Icons.bar_chart, color: Colors.white, size: 28),
                  const SizedBox(width: 10),
                  const Text(
                    'Generate Reports',
                    style: TextStyle(
                      fontSize: 22,
                      fontWeight: FontWeight.bold,
                      color: Colors.white,
                    ),
                  ),
                ],
              ),
              IconButton(
                icon: const Icon(Icons.picture_as_pdf, color: Colors.white),
                onPressed: _exportPdfReport,
              )
            ],
          ),
        ),
      ),
      body: Padding(
        padding: const EdgeInsets.all(20.0),
        child: SingleChildScrollView(
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.stretch,
            children: [
              _buildReportCard('Total Customers', totalCustomers, Icons.people),
              const SizedBox(height: 20),
              _buildReportCard('Total Shops', totalShops, Icons.store),
              const SizedBox(height: 20),
              _buildReportCard('Total Repair Requests', totalRepairRequests, Icons.build),
              const SizedBox(height: 20),
              _buildReportCard("Today's New Repair Requests", todaysRepairRequests, Icons.today),
              const SizedBox(height: 20),
              _buildReportCard('Completed Repairs', completedRepairs, Icons.check_circle),
              const SizedBox(height: 20),
              _buildReportCard('Pending Repairs', pendingRepairs, Icons.pending_actions),
              const SizedBox(height: 30),
              const Text(
                'Repair Requests Overview',
                style: TextStyle(fontSize: 20, fontWeight: FontWeight.bold),
                textAlign: TextAlign.center,
              ),
              const SizedBox(height: 20),
              PieChart(
                dataMap: dataMap,
                colorList: [Colors.green, Colors.orange],
                chartRadius: MediaQuery.of(context).size.width / 2.2,
                chartValuesOptions: const ChartValuesOptions(showChartValuesInPercentage: true),
                legendOptions: const LegendOptions(legendPosition: LegendPosition.right),
              ),
            ],
          ),
        ),
      ),
    );
  }

  Widget _buildReportCard(String title, int count, IconData icon) {
    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(20),
        boxShadow: [
          BoxShadow(
            color: Colors.grey.withOpacity(0.2),
            spreadRadius: 2,
            blurRadius: 5,
            offset: const Offset(0, 3),
          ),
        ],
      ),
      child: Row(
        children: [
          Icon(icon, size: 40, color: Colors.green),
          const SizedBox(width: 20),
          Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                title,
                style: const TextStyle(fontSize: 18, fontWeight: FontWeight.bold),
              ),
              const SizedBox(height: 6),
              Text(
                count.toString(),
                style: const TextStyle(fontSize: 16, color: Colors.black54),
              ),
            ],
          ),
        ],
      ),
    );
  }
}
